class TextAry
{
	public static void main(String[] args) 
	{
		int s [];
		s = new int[5];
		for (int i=0; i<5 ; i++ )
		{
			s[i] = 2*i + 1;
			System.out.println(s[i]);
	    }
		int[] a = {2,3,4,5,7};
		for (int i=0; i<a.length ; i++ )
		{
			System.out.println(a[i]);
		}
		for (int i=0; i<args.length ;i++ )
		{
			System.out.println(args[i]);
		}
	}
		
}
